using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Question_02
{
    class FindValues
    {
        public double findArea(double r)
        {
            double a = 3.14 * r*r;  

            return a;
        }

        public double findCircumference(double r)
        {
            double c = 2 * 3.14 * r;

            return c;
        }
    }
}
